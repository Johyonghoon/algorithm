# 11726 2 x n 타일링
# https://www.acmicpc.net/problem/11726

n = int(input())
d = [0 for _ in range(n+1)]
d[0:2] = [1, 1]
for i in range(2, n+1):
    if d[i] !=0:
        continue
    d[i] = d[i-1] + d[i-2]
print(d[n] % 10007)

