# 15989번 1, 2, 3 더하기 4
# https://www.acmicpc.net/problem/15989

"""
1 1개
(0, 0, 1)                       1 // 2 = 0 + 1

2 2개
(0, 0, 2) (0, 1, 0)             2 // 2 = 1 + 1

3 3개
(0, 0, 3) (0, 1, 1)             3 // 2 = 1 + 1
(1, 0, 0)                       0 // 2 = 0 + 1

4 4개
(0, 0, 4) (0, 1, 2) (0, 2, 0)   4 // 2 = 2 + 1
(1, 0, 1)                       1 // 2 = 0 + 1
5 5개
(0, 0, 5) (0, 1, 3) (0, 2, 1)
(1, 0, 2) (1, 1, 0)

6 7개
(0, 0, 6) (0, 1, 4) (0, 2, 2) (0, 3, 0)
(1, 0, 3) (1, 1, 1)
(2, 0, 0)

7 8개
(0, 0, 7) (0, 1, 5) (0, 2, 3) (0, 3, 1)
(1, 0, 4) (1, 1, 2) (1, 2, 0)
(2, 0, 0)

8 10개
(0, 0, 8) (0, 1, 6) (0, 2, 4) (0, 3, 2) (0, 4, 0)
(1, 0, 5) (1, 1, 3) (1, 2, 1)
(2, 0, 2) (2, 1, 0)

9 12개
(0, 0, 9) (0, 1, 7) (0, 2, 5) (0, 3, 3) (0, 4, 1)
(1, 0, 6) (1, 1, 4) (1, 2, 2) (1, 3, 0)
(2, 0, 3) (2, 1, 1)
(3, 0, 0)

10 14개

11
(0, 0, 11) (0, 1, 9) (0, 2, 7) (0, 3, 5) (0, 4, 3) (0, 5, 1)    11 // 2 = 5 + 1
(1, 0, 8) (1, 1, 6) (1, 2, 4) (1, 3, 3) (1, 4, 1)               8 // 2 = 4 + 1
(2, 0, 5) (2, 1, 3) (2, 2, 1)                                   5 // 2 = 2 + 1
(3, 0, 2) (3, 1, 0)                                             2 // 2 = 1 + 1
"""

T = int(input())
for _ in range(T):
    N = int(input())
    cnt = 0
    while N >= 0:
        cnt += N // 2 + 1
        N -= 3
    print(cnt)
